// ─── Componente Navbar ────────────────────────────────────────────────────────
export default function Navbar({ current }: { current: NavTab }) {
  const router = useRouter();

  const tabs: { key: NavTab; route: string; icon: (sel: boolean) => React.ReactElement }[] = [
    { key: "home",    route: "/home",    icon: (s) => <IconHome selected={s} /> },
    { key: "receitas",route: "/receitas",icon: (s) => <IconReceitas selected={s} /> },
    { key: "gravida", route: "/maeinfo", icon: (s) => <IconGravida selected={s} /> },
    { key: "perfil",  route: "/perfil", icon: (s) => <IconPerfil selected={s} /> },
  ];

  return (
    <View style={styles.navbar}>
      {tabs.map((tab) => {
        const isSelected = current === tab.key;
        return (
          <TouchableOpacity
            key={tab.key}
            style={styles.navItem}
            onPress={() => {
              if (!isSelected) router.push(tab.route as any);
            }}
            activeOpacity={0.7}
          >
            <View style={[styles.iconWrap, isSelected && styles.iconWrapSelected]}>
              {tab.icon(isSelected)}
            </View>
          </TouchableOpacity>
        );
      })}
    </View>
  );
}

